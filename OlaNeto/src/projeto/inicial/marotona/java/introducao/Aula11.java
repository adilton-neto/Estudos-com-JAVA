/**
 *  <h1>Aula 11 - Estruturas de repetição</h1>
 */

public class Aula11 {
    public static void main(String[] args) {

        // whilevai tudo datr; do while ; for
        int contador = 0;
        while(contador <= 10){ //tem que receber valor boulean igual o if // ou SEJA enquanto o parametro for verdadeiro ele vai ficar execultando
            System.out.println(contador);
            contador++;

        }

        // for

        for (int i=0; i<=10; i++){
            System.out.println("for "+i);
        }


    }


}
