package org.aplicacao;

public class ArrayBasic {
    public static void main(String[] args) {
        System.out.println("Hello world!");
        String[] nomes = {
                "neto", "laisla", "mirele", "pereira"
        };
        for(int i=0;i<nomes.length; i++ ){
            System.out.println(nomes[i]);
        }
    }
}

